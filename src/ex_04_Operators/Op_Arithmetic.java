package ex_04_Operators;

public class Op_Arithmetic {
    public static void main(String[] args) {

        int a=20;
        int b=30;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        float c=3.0f;
        System.out.println(a/c);
        System.out.println("a+b");
    }
}
