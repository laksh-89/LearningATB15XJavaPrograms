package ex_08_Switch;

public class Interview_question {
    public static void main(String[] args) {
        char code= 'c';
        switch (code) {
            default:
                System.out.println("Hellooooooo");
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("68");
        }
    }
}
